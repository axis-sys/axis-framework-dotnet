#!/usr/bin/env node
'use strict';
// bundle-plugin — materialize the canonical sources INSIDE the installable plugin subtree.
//
// The marketplace installs only skills/plugins/axis-dotnet/**; rules/, docs/ and src/scaffolds/ live at
// the repo root, so every deep-relative SKILL.md link (../../../../../rules/...) is dead in a real
// installation. This script copies the rule base, the docs and the scaffold sources into the plugin
// (rules/, docs/, scaffolds/) and rewrites the SKILL.md links to the bundled copies, so a downstream
// agent working from the installed plugin alone can resolve every reference offline.
//
// Modes:
//   node skills/scripts/bundle-plugin.mjs           # write: copy + rewrite + prune stale files
//   node skills/scripts/bundle-plugin.mjs --check   # CI gate: exit 1 when the committed bundle drifts
// Zero dependencies.

import { readdirSync, readFileSync, writeFileSync, mkdirSync, rmSync, existsSync, statSync } from 'node:fs';
import { join, dirname, relative, sep } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO_ROOT = join(dirname(fileURLToPath(import.meta.url)), '..', '..');
const PLUGIN_DIR = join(REPO_ROOT, 'skills', 'plugins', 'axis-dotnet');
const CHECK = process.argv.includes('--check');

const rel = (p) => relative(REPO_ROOT, p).split(sep).join('/');

function walk(dir, out = []) {
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) {
      if (['bin', 'obj', '.idea', '.vs', 'node_modules'].includes(entry)) continue;
      walk(full, out);
    } else {
      out.push(full);
    }
  }
  return out;
}

// ---------- expected bundle: dest path (inside PLUGIN_DIR) -> content ----------
const expected = new Map();

// rules/ — every rule yaml plus the schema, guide and generated index; tooling scripts stay out.
for (const file of walk(join(REPO_ROOT, 'rules'))) {
  const r = rel(file);
  if (r.startsWith('rules/tooling/')) continue;
  if (!/\.(yaml|md)$/.test(r)) continue;
  expected.set(r, readFileSync(file, 'utf-8'));
}

// docs/ — the navigable documentation (en + pt), markdown only.
for (const file of walk(join(REPO_ROOT, 'docs'))) {
  const r = rel(file);
  if (!r.endsWith('.md')) continue;
  expected.set(r, readFileSync(file, 'utf-8'));
}

// scaffolds/ — the reference sample's sources, csproj/slnx/config included (rule anchors cite them).
for (const file of walk(join(REPO_ROOT, 'src', 'scaffolds'))) {
  const r = rel(file);
  if (!/\.(cs|csproj|slnx|json|props)$/.test(r)) continue;
  expected.set(r.replace(/^src\/scaffolds\//, 'scaffolds/'), readFileSync(file, 'utf-8'));
}

expected.set('BUNDLE.md', `# Bundled canonical sources

This plugin ships a verbatim copy of the framework's canonical sources so every reference resolves
offline, from the installed plugin alone:

| In this bundle | Mirrors (framework repo) |
|---|---|
| \`rules/\` | \`rules/\` — the canonical rule base (one YAML per rule) |
| \`docs/\` | \`docs/\` — the navigable documentation (en-us + pt-br) |
| \`scaffolds/\` | \`src/scaffolds/\` — the compiled, tested reference application |

Anchors inside rule files (\`reference.at\`, \`examples.correct\`) that cite \`src/scaffolds/...\`
resolve against \`scaffolds/...\` here. Anchors into the framework's own source or tests
(\`src/0-Foundations/...\`, \`src/2-ApplicationFlow/...\`, ...) are **not** bundled — they resolve only
in the framework repository itself. The scaffold copy is reference-only: it omits \`bin/\`/\`obj/\`
and the framework projects it builds against, so it is not buildable in place.

Generated by \`skills/scripts/bundle-plugin.mjs\`; CI fails when this bundle drifts from the sources
(\`bundle-plugin.mjs --check\`). Do not edit by hand.
`);

// ---------- SKILL.md link rewriting (part of the expected state) ----------
const LINK_REWRITES = [
  ['](../../../../../rules/', '](../../rules/'],
  ['](../../../../../docs/', '](../../docs/'],
  ['](../../../../../src/scaffolds/', '](../../scaffolds/'],
];

const skillFiles = [];
const skillsDir = join(PLUGIN_DIR, 'skills');
for (const name of readdirSync(skillsDir)) {
  const file = join(skillsDir, name, 'SKILL.md');
  if (existsSync(file)) skillFiles.push(file);
}

let drift = 0;
const report = (path) => { console.log(`DRIFT ${path}`); drift++; };

// 1) SKILL.md rewrites
let rewritten = 0;
for (const file of skillFiles) {
  const current = readFileSync(file, 'utf-8');
  let next = current;
  for (const [from, to] of LINK_REWRITES) next = next.split(from).join(to);
  if (next !== current) {
    if (CHECK) report(rel(file));
    else { writeFileSync(file, next); rewritten++; }
  }
}

// 2) prune stale bundled files FIRST (before writing, so a case-only rename on a case-insensitive
// filesystem deletes the old-cased file instead of silently writing into it)
let pruned = 0;
for (const root of ['rules', 'docs', 'scaffolds']) {
  const dir = join(PLUGIN_DIR, root);
  if (!existsSync(dir)) continue;
  for (const file of walk(dir)) {
    const destRel = relative(PLUGIN_DIR, file).split(sep).join('/');
    if (!expected.has(destRel)) {
      if (CHECK) report(rel(file));
      else { rmSync(file); pruned++; }
    }
  }
}

// 3) bundle content
let written = 0;
for (const [destRel, content] of expected) {
  const dest = join(PLUGIN_DIR, destRel);
  const onDisk = existsSync(dest) ? readFileSync(dest, 'utf-8') : null;
  if (onDisk !== content) {
    if (CHECK) report(rel(dest));
    else {
      mkdirSync(dirname(dest), { recursive: true });
      writeFileSync(dest, content);
      written++;
    }
  }
}

if (CHECK) {
  console.log(`\nbundle-plugin --check: ${drift} drift file(s).`);
  process.exit(drift ? 1 : 0);
} else {
  console.log(`\nbundle-plugin: ${expected.size} file(s) expected, ${written} written, ${pruned} pruned, ${rewritten} SKILL.md rewritten.`);
}
